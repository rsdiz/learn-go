package main

func main() {
	/**
	Constant is a variable whose value cannot be changed again
	after it is first assigned a value

	To create constant, use keyword "const",
	and we must immediately initialize with data
	*/

	const name = "Muhammad Rosyid Izzulkhaq"

	// Like in variable, we can declare multiple constant

	const (
		currentYear  uint16 = 2024
		currentMonth uint8  = 3
		currentDate  uint8  = 2
	)
}
