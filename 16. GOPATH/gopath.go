/**
GOPATH
is an environment variable that contains the location where we store Go-Lang projects and libraries.
GOPATH is important to create when we start the project with many files or need to use library from outside.

to set GOPATH,
- edit .bashrc or .zshrc (mac or linux) / edit environment variable (windows)
- add this: (directory can be changed to what we want)
	export GOPATH="/Users/name/Development/GOLANG"
- create 'src' directory in that path, and move your current project (or create new project) on it.
*/
